package com.projeto.variaveisjava;

public class Variavel {
	
	//variáveis
	
	//byte - Números - 1,2,3 (range) byte: é capaz de armazenar valores entre -128 até 127.
	
	//short: é capaz de armazenar valores entre – 32768 até 32767.
	
	//int - Números - 1000, 23003, 2, 1 (é capaz de armazenar valores entre –2147483648 até 2147483647.)
	
	//Long - Números - 100000000000, 2 ,1 (long: é capaz de armazenar valores entre –9223372036854775808 até 9223372036854775807.)
	
	
	//float - Números decimais - 1.1234, 2.34561 (float: é capaz de armazenar valores entre 3.4e–038 até 3.4e+038)
	
	//double - Números decimais o dobro - 1.123132123  (double: é capaz de armazenar valores entre 1 .7e–308 até 1.7e+308)
	
	//boolean - valores true/false
	
	//String - armazenar textos
	
	

	public static void main(String[] args) {
		
		byte minhaVariavelByte = 14;
		
		int minhaVariavelInt = 10000000;
		
		long minhaVariavellong = 100000000;
		
		float minhaVariavelfloat = 1.5f;
		
		double minhaVariaveldouble = 1.512313213;
		
		boolean minhaVariavelBoolean = false;
		
		String minhaVariavelstring = "cocacola 1 + 1  - ";
		
		
		
		System.out.println(minhaVariavelByte);
		
		System.out.println(minhaVariavelInt);
		
		System.out.println(minhaVariavellong);
		
		System.out.println(minhaVariaveldouble);
		
		System.out.println(minhaVariavelBoolean);
		
		System.out.println(minhaVariavelstring + minhaVariavelByte);
		
		
		

	}

}
